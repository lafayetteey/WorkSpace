	public static void testFor5() {
		/*
		한줄에 별표문자를 입력된 줄수와 칸수 만큼 출력
		1*****
		*2****
		**3***
		***4**
		****5*
		*****6
		*/
		Scanner sc = new Scanner(System.in);
		
		System.out.print("숫자 입력: " );
		int num = sc.nextInt();
		
		for( int i=1; i<=num; i++) {
			for (int j =1; j <= num; j++) {
				if(i == j) {
					System.out.print(j);
				} else {
					System.out.print("*");
				}
			}
			System.out.println();
		}
		System.out.print("줄 수: ");
		int row = sc.nextInt();
		System.out.print("칸 수: ");
		int col = sc.nextInt();
		
		for(int r = 1; r <= row; r++) {
			for(int c =1; c <= col; c++) {
				if(r == c ) {
					System.out.print(c);
				} else {
					System.out.print("*");
				}			
			}		
			System.out.println();
		}
	}

	
